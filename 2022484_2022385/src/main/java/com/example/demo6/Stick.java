package com.example.demo6;

public class Stick {
    private double posX;
    private double posY;
    private double height;

    public double getPosX() {
        return this.posX;
    }

    public double getPosY() {
        return this.posY;
    }

    public void setPosX(double x) {
        this.posX = x;
    }

    public void setPosY(double y) {
        this.posY = y;
    }

    public double getHeight() {
        return this.height;
    }

    public void setHeight(double h) {
        this.height = h;
    }
}

